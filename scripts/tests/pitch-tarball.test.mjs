const HERE = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(HERE, '..', '..');

async function tmpDir(label) {
  const d = resolve(tmpdir(), `${label}-${Date.now()}-${Math.random().toString(16).slice(2, 8)}`);
  await fs.mkdir(d, { recursive: true });
  return d;
}

describe('pitch tarball parseArgs', () => {
  it('defaults to tar.gz format under dist/pitch', () => {
    const a = parseArgs(['node', 'x']);
    expect(a.format).toBe('tar.gz');
    expect(a.output).toBe('dist/pitch');
    expect(a.dryRun).toBe(false);
    expect(a.includeBinaries).toBe(false);
    expect(a.sign).toBe(false);
  });

  it('parses --format=zip, --output=, --operator=, --bundle-version=, --sign, --dry-run', () => {
    const a = parseArgs([
      'node', 'x',
      '--format=zip',
      '--output=/tmp/o',
      '--operator=Vendor C',
      '--bundle-version=v20990101',
      '--sign',
      '--dry-run',
    ]);
    expect(a.format).toBe('zip');
    expect(a.output).toBe('/tmp/o');
    expect(a.operator).toBe('Vendor C');
    expect(a.bundleVersion).toBe('v20990101');
    expect(a.sign).toBe(true);
    expect(a.dryRun).toBe(true);
  });

  it('throws on unsupported format', () => {
    expect(() => parseArgs(['node', 'x', '--format=rar'])).toThrow(/unsupported format/);
  });
});

describe('pitch tarball metadata helpers', () => {
  it('sha256Hex of empty buffer is the canonical empty-sha', () => {
    expect(sha256Hex(Buffer.alloc(0))).toBe(
      'e3b0c44298fc1c149afbf4c8996fb92427ae41e4649b934ca495991b7852b855'
    );
  });

  it('guessMimeType maps common extensions', () => {
    expect(guessMimeType('foo.html')).toBe('text/html');
    expect(guessMimeType('a/b.md')).toBe('text/markdown');
    expect(guessMimeType('a.json')).toBe('application/json');
    expect(guessMimeType('a.bin')).toBe('application/octet-stream');
  });

  it('hashFileEntry returns path/size/sha256/mime', () => {
    const e = hashFileEntry({ bundlePath: 'x.txt', data: Buffer.from('abc') });
    expect(e.path).toBe('x.txt');
    expect(e.size).toBe(3);
    expect(e.sha256).toBe('ba7816bf8f01cfea414140de5dae2223b00361a396177a9cb410ff61f20015ad');
    expect(e.mime).toBe('text/plain');
  });

  it('deriveBundleFilename composes safe filename', () => {
    expect(deriveBundleFilename({ bundleVersion: 'v20260518', commitShort: 'abc12345', format: 'tar.gz' }))
      .toBe('slot-math-engine-pitch-v20260518-abc12345.tar.gz');
  });

  it('resolveGitInfo returns shape with commit + branch', async () => {
    const g = await resolveGitInfo({ root: REPO_ROOT });
    expect(typeof g.commit).toBe('string');
    expect(typeof g.commitShort).toBe('string');
    expect(typeof g.branch).toBe('string');
    expect(g.commitShort.length).toBeLessThanOrEqual(8);
  });
});

describe('pitch tarball — manifest builder', () => {
  const baseOpts = {
    engineVersion: '1.0.0',
    bundleVersion: 'v20990101',
    format: 'tar.gz',
    git: { commit: 'a'.repeat(40), commitShort: 'aaaaaaaa', branch: 'main' },
    timestamp: '2099-01-01T00:00:00.000Z',
  };

  it('builds manifest with schema + entry hashes', () => {
    const m = buildManifest({
      ...baseOpts,
      entries: [
        { bundlePath: 'b.txt', data: Buffer.from('bb') },
        { bundlePath: 'a.txt', data: Buffer.from('aa') },
      ],
    });
    expect(m.schema).toBe('pitch-tarball-manifest-v1');
    expect(m.files.length).toBe(2);
    // Sorted alphabetically.
    expect(m.files[0].path).toBe('a.txt');
    expect(m.files[1].path).toBe('b.txt');
    expect(m.counts.fileCount).toBe(2);
    expect(m.counts.totalSizeBytes).toBe(4);
  });

  it('is deterministic given same inputs', () => {
    const e = [{ bundlePath: 'a', data: Buffer.from('xx') }];
    const m1 = buildManifest({ ...baseOpts, entries: e });
    const m2 = buildManifest({ ...baseOpts, entries: e });
    expect(JSON.stringify(m1)).toBe(JSON.stringify(m2));
  });

  it('throws if required field missing', () => {
    expect(() => buildManifest({})).toThrow();
  });
});

