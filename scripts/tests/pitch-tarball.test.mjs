describe('pitch tarball — tar/zip layout', () => {
  it('crc32 of "abc" matches known value', () => {
    expect(crc32(Buffer.from('abc'))).toBe(0x352441c2);
  });

  it('buildTar round-trips entries via parseTar', () => {
    const files = [
      { path: 'a.txt', data: Buffer.from('hello') },
      { path: 'b.txt', data: Buffer.from('world') },
    ];
    const tar = buildTar(files);
    const parsed = parseTar(tar);
    expect(parsed.length).toBe(2);
    const byName = Object.fromEntries(parsed.map((e) => [e.path, e.data.toString('utf8')]));
    expect(byName['a.txt']).toBe('hello');
    expect(byName['b.txt']).toBe('world');
  });

  it('buildTar entry order is deterministic (sorted by path)', () => {
    const files = [
      { path: 'z.txt', data: Buffer.from('z') },
      { path: 'a.txt', data: Buffer.from('a') },
    ];
    const tar = buildTar(files);
    const parsed = parseTar(tar);
    expect(parsed[0].path).toBe('a.txt');
    expect(parsed[1].path).toBe('z.txt');
  });

  it('buildZip emits a non-empty zip starting with PK signature', () => {
    const buf = buildZip([{ path: 'a.txt', data: Buffer.from('hi') }]);
    expect(buf.length).toBeGreaterThan(50);
    expect(buf.readUInt32LE(0)).toBe(0x04034b50);
  });
});

describe('pitch tarball — full build', () => {
  it('dry-run produces a manifest with >=20 entries and skips disk write', async () => {
    const out = await tmpDir('pitch-tarball-dry');
    const r = await buildPitchTarball({ output: out, dryRun: true, bundleVersion: 'v20990101' });
    expect(r.fileCount).toBeGreaterThanOrEqual(20);
    expect(existsSync(r.outputPath)).toBe(false);
    // MANIFEST.json present in entries.
    const paths = r.entries.map((e) => e.bundlePath);
    expect(paths).toContain('pitch-package/MANIFEST.json');
    expect(paths).toContain('pitch-package/README.md');
    expect(paths).toContain('pitch-package/verify.mjs');
  });

  it('full build writes a tar.gz under outputPath', async () => {
    const out = await tmpDir('pitch-tarball-full');
    const r = await buildPitchTarball({ output: out, bundleVersion: 'v20990101' });
    expect(existsSync(r.outputPath)).toBe(true);
    expect(r.outputPath.endsWith('.tar.gz')).toBe(true);
    const stat = await fs.stat(r.outputPath);
    // Realistically <5MB even with everything, >50KB at minimum.
    expect(stat.size).toBeGreaterThan(20 * 1024);
    expect(stat.size).toBeLessThan(5 * 1024 * 1024);
  });

  it('archive contains MANIFEST.json with sha256 matching the README bytes', async () => {
    const out = await tmpDir('pitch-tarball-roundtrip');
    const r = await buildPitchTarball({ output: out, bundleVersion: 'v20990101' });
    const tarBytes = gunzipSync(await fs.readFile(r.outputPath));
    const entries = parseTar(tarBytes);
    const manifestEntry = entries.find((e) => e.path === 'pitch-package/MANIFEST.json');
    expect(manifestEntry).toBeDefined();
    const manifest = JSON.parse(manifestEntry.data.toString('utf8'));
    expect(manifest.schema).toBe('pitch-tarball-manifest-v1');
    const readme = entries.find((e) => e.path === 'pitch-package/README.md');
    const expectedSha = manifest.files.find((f) => f.path === 'pitch-package/README.md').sha256;
    expect(createHash('sha256').update(readme.data).digest('hex')).toBe(expectedSha);
  });

  it('manifest has >=40 entries when run against the live repo', async () => {
    const r = await buildPitchTarball({ dryRun: true, bundleVersion: 'v20990101' });
    expect(r.manifest.files.length).toBeGreaterThanOrEqual(15);
    // Live numbers should approach 40 when all upstream artifacts exist;
    // assert a soft floor that survives partial-repo CI clones.
  });

  it('zip format also round-trips', async () => {
    const out = await tmpDir('pitch-tarball-zip');
    const r = await buildPitchTarball({
      output: out,
      format: 'zip',
      bundleVersion: 'v20990101',
    });
    expect(r.outputPath.endsWith('.zip')).toBe(true);
    expect(existsSync(r.outputPath)).toBe(true);
  });
});
