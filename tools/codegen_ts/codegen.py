def write_ts_codegen(
    *,
    ir: dict[str, Any],
    out_dir: Path,
    slug: str | None = None,
) -> dict[str, Path]:
    """Emit the <slug>-ts package layout. Returns map of role → Path."""
    if slug is None:
        slug = slugify(str(ir.get("meta", {}).get("id") or "game"))
    slug = slugify(slug)
    crate_dir = Path(out_dir) / f"{slug}-ts"
    src_dir = crate_dir / "src"
    ir_dir = crate_dir / "ir"
    tests_dir = crate_dir / "tests"
    for d in (src_dir, ir_dir, tests_dir):
        d.mkdir(parents=True, exist_ok=True)

    pkg_path = crate_dir / "package.json"
    pkg_path.write_text(_PACKAGE_JSON.substitute(slug=slug))

    tsconfig_path = crate_dir / "tsconfig.json"
    tsconfig_path.write_text(_TSCONFIG)

    sim_path = src_dir / "sim.ts"
    sim_path.write_text(_SIM_TS.substitute(slug=slug))

    main_path = src_dir / "main.ts"
    main_path.write_text(_MAIN_TS.substitute(slug=slug))

    ir_path = ir_dir / f"{slug}.ir.json"
    ir_path.write_text(json.dumps(ir, indent=2, sort_keys=True))

    spec_path = tests_dir / f"{slug}.spec.ts"
    spec_path.write_text(_SPEC_TS.substitute(slug=slug))

    readme_path = crate_dir / "README.md"
    readme_path.write_text(_README.substitute(slug=slug))

    return {
        "crate_dir": crate_dir,
        "package_json": pkg_path,
        "tsconfig": tsconfig_path,
        "sim_ts": sim_path,
        "main_ts": main_path,
        "ir_json": ir_path,
        "spec_ts": spec_path,
        "readme": readme_path,
    }
