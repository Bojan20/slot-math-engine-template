#!/usr/bin/env python3
"""W244 wave 47 — Rust kernel benchmark dossier.

Aggregates criterion benchmark estimates into a single JSON dossier with
Merkle root attestation. Provides marketing/regulator-friendly evidence
of sub-microsecond performance baseline across the W244 kernel fleet.

Source: `target/criterion/<group>/<bench_name>/new/estimates.json` —
generated by `cargo bench --bench w244_kernel_bench`.

Output: `reports/acceptance/W244_BENCHMARK_DOSSIER.json`
"""
from __future__ import annotations

import hashlib
import json
from pathlib import Path

REPO = Path(__file__).resolve().parents[1]
CRITERION_ROOT = REPO / "target" / "criterion"
OUT = REPO / "reports" / "acceptance" / "W244_BENCHMARK_DOSSIER.json"
OUT.parent.mkdir(parents=True, exist_ok=True)


def _collect_estimates() -> list[dict]:
    """Walk criterion output tree, collect per-bench mean point estimates."""
    records = []
    if not CRITERION_ROOT.exists():
        return records
    for est_file in sorted(CRITERION_ROOT.glob("**/new/estimates.json")):
        # Path shape: target/criterion/<group>/<bench>/new/estimates.json
        rel = est_file.relative_to(CRITERION_ROOT)
        parts = rel.parts
        if len(parts) < 4:
            continue
        group = parts[0]
        bench = "/".join(parts[1:-2]) if len(parts) > 4 else parts[1]
        # Filter for W244 (kernels/* + solvers/* + dp/*)
        if group not in {"kernels", "solvers", "dp"}:
            continue
        try:
            est = json.loads(est_file.read_text())
            mean_ns = est["mean"]["point_estimate"]
            stderr_ns = est["mean"].get("standard_error", 0.0)
            records.append({
                "group": group,
                "bench": bench,
                "mean_ns": mean_ns,
                "stderr_ns": stderr_ns,
                "ops_per_sec": 1e9 / mean_ns if mean_ns > 0 else 0,
            })
        except (json.JSONDecodeError, KeyError):
            continue
    return records


def main() -> int:
    records = _collect_estimates()
    if not records:
        print("[w244-benchmark] no criterion outputs found — "
              "run `cargo bench --bench w244_kernel_bench` first")
        return 1

    # Stable sort for byte-stable Merkle
    records.sort(key=lambda r: (r["group"], r["bench"]))

    leaf_lines = [
        f"{r['group']}/{r['bench']}|"
        f"mean_ns={r['mean_ns']:.6e}|"
        f"ops_per_sec={r['ops_per_sec']:.6e}\n"
        for r in records
    ]
    merkle_root = hashlib.sha256(
        "".join(leaf_lines).encode("utf-8")
    ).hexdigest()

    # Headline metrics
    fastest = min(records, key=lambda r: r["mean_ns"])
    slowest = max(records, key=lambda r: r["mean_ns"])
    mean_per_bench = sum(r["mean_ns"] for r in records) / len(records)

    artefact = {
        "schema": "w244-benchmark-dossier/v1",
        "merkle_root_sha256": merkle_root,
        "generated_at_utc": f"deterministic-by-merkle:{merkle_root[:16]}",
        "bench_count": len(records),
        "fastest": {
            "bench": f"{fastest['group']}/{fastest['bench']}",
            "mean_ns": fastest["mean_ns"],
            "ops_per_sec": fastest["ops_per_sec"],
        },
        "slowest": {
            "bench": f"{slowest['group']}/{slowest['bench']}",
            "mean_ns": slowest["mean_ns"],
            "ops_per_sec": slowest["ops_per_sec"],
        },
        "mean_across_all_benches_ns": mean_per_bench,
        "all_sub_microsecond": all(r["mean_ns"] < 1000.0 for r in records),
        "records": records,
        "tooling": {
            "runner": "criterion 0.x (cargo bench)",
            "input": "rust-sim/benches/w244_kernel_bench.rs",
            "rust_profile": "release",
            "host_cpu": "Apple M-series (Darwin arm64) — see local hardware",
        },
        "verification": (
            "Re-run `cargo bench --bench w244_kernel_bench`. After completion, "
            "re-run `python3 tools/build_benchmark_dossier.py`. Output "
            "merkle_root will rotate (estimates.json contains live "
            "measurements; this dossier is snapshot-only)."
        ),
    }
    OUT.write_text(json.dumps(artefact, ensure_ascii=False, indent=2))
    print(f"[w244-benchmark] wrote {OUT.relative_to(REPO)}")
    print(f"  bench count:           {len(records)}")
    print(f"  fastest:               {fastest['group']}/{fastest['bench']:30s}  "
          f"{fastest['mean_ns']:.1f} ns")
    print(f"  slowest:               {slowest['group']}/{slowest['bench']:30s}  "
          f"{slowest['mean_ns']:.1f} ns")
    print(f"  mean across all:       {mean_per_bench:.1f} ns")
    print(f"  all sub-microsecond:   {artefact['all_sub_microsecond']}")
    print(f"  merkle root:           {merkle_root}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
