def generate_gdd(prompt, *, client=None, model=None, use_cache=True,
                 cache_dir=None):
    """High-level entry: prompt → cached/LLM JSON → validated GDD YAML.

    Imported here (after the leaf-module imports) to avoid circulars.
    """
