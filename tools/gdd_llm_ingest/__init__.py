"""W6.2 — LLM-assisted NL → GDD ingestion (Anthropic Claude).

Sibling of W6.1 ``tools.gdd_nl_ingest`` (deterministic regex ingester).
W6.2 calls the Anthropic Claude API with a strict tool-use schema and
compiles the structured JSON response into a canonical GDD YAML that
``tools.math_dsl.spec.parse_spec`` accepts.

Design contract
===============
* Determinism — ``temperature=0`` + ``top_k=1`` + sorted-key JSON +
  deterministic float formatting => same prompt produces a bit-identical
  GDD across runs (modulo a cache hit).
* Offline-safe — when ``ANTHROPIC_API_KEY`` is missing the CLI prints
  a clear fallback message and shells out to the W6.1 deterministic
  ingester.
* Cached — every (prompt, model, schema_version) triple hashes into
  ``reports/gdd-llm-cache/<sha256>.json``.  Cache hits skip the API
  call entirely.
* Prompt caching — the system prompt + few-shot exemplar block are
  marked ``cache_control: ephemeral`` so repeated invocations within
  the 5-minute TTL hit Anthropic's prompt cache.

Public API
==========

>>> from tools.gdd_llm_ingest import generate_gdd
>>> result = generate_gdd("design a 96% RTP wolf-themed lines game",
...                       client=mock_client)
>>> result.gdd_yaml.startswith("# GDD generated by W6.2 LLM ingest")
True
"""

